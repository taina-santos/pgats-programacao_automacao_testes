/**
 * Operadores:
 * Serve para operações aritméticas, comparação ou lógicas
 * 
 * -- Artiméticos
 * + - adição
 * - - subtração
 * * - multiplicação
 * / - divisão
 * % - resto
 * ** - exponencial
 * 
 */

console.log(`1 + 2 = ${ 1 + 2}`) // adição
console.log(`5 - 2 = ${ 5 - 2}`) // subtração
console.log(`5 * 2 = ${ 5 * 2}`) // multiplicação
console.log(`16 / 2 = ${ 16 / 2}`) // divisão
console.log(`5 % 2 = ${ 5 % 2}`) // resto
console.log(`3 ** 2 = ${ 3 ** 2}`) // exponencial